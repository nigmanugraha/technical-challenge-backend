export const emailRegex =
  /^[a-zA-Z0-9_.+]+(?<!^[0-9].)@[a-zA-Z0-9-]+\.[a-zA-Z0-9-.]+$/;

/**
 * note that this regex only match with main url with protocol http/https only,
 * http://facebook.com (match)
 * https://facebook.co.id (match)
 * facebook.com (not match)
 * https://facebok.com/home (not match)
 */
export const websiteRegex =
  /https?:\/\/(www\.)?[-a-zA-Z0-9@:%._\+~#=]{2,256}\.[a-z]{2,6}\b/;
export const urlRegex =
  /[-a-zA-Z0-9@:%._\+~#=]{2,256}\.[a-z]{2,6}\b([-a-zA-Z0-9@:%_\+.~#?&//=]*)/g;
export const objectIdRegex = /^[0-9a-fA-F]{24}$/;
export const imageUrlRegex =
  /(http(s?):)([/|.|\w|\s|-])*\.(?:jpg|gif|png|webp|jpeg|tif)/;
export const time24Regex = /^([0-9]{2})\:([0-9]{2})$/;

